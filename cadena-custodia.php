<h2>Compromiso de la Dirección</h2>
<p>
Morés, empresa de artes gráficas dedicada a la edición de manuales y publicaciones, la elaboración de revistas, folletos publicitarios, trípticos, cartelería, así como cartas, tarjetas de visita, memorias y demás productos del campo interno de las empresas, ha decidido implementar y mantener un Sistema de Gestión de Cadena de Custodia, de acuerdo con los requisitos de las siguientes normas:
</p>
<ul>
  <li>Estándar <b>FSC</b> para certificación de Cadena de Custodia FSC-STD-40-004 (Versión 2-1).</li>
  <li>Norma Internacional <b>PEFC</b>. Cadena de Custodia de Productos Forestales. Requisitos. PEFC ST 2002: 2010</li>
</ul>
<p>
Por ello, Morés ha elaborado un sistema que integra unos requisitos con los que pretende satisfacer las exigencias y requisitos que se derivan de estas normas.
</p>
<p>
La Dirección de Morés declara su compromiso de cumplir en todo momento con los valores de FSC tal y como se define en la Política para la Asociación de Organizaciones FSC (FSC-POL-01-004), en concreto la Empresa declara no estar directa o indirectamente involucrada en las siguientes actividades:
</p>
<ol type="a">
  <li>La tala ilegal y el comercio de madera ilegal de productos forestales;</li>
  <li>Violación de los derechos tradicionales y los derechos humanos en las operaciones forestales;</li>
  <li>Destrucción de los altos valores de conservación en las operaciones forestales;</li>
  <li>La conversión significativa de bosques a plantaciones u otros usos no forestales;</li>
  <li>La introducción de organismos modificados genéticamente en las operaciones forestales</li>
  <li>La violación de cualquiera de los Convenios fundamentales de la OIT, tal como se define en la OIT Declaración sobre Principios y Derechos Fundamentales en el Trabajo, 1998.</li>
</ol>
<p>

La Empresa declara su compromiso de cumplir con los requisitos sociales y de seguridad y salud laboral definidos en ambas normas de cadena de custodia, tal y como se recoge en su Código de Conducta con los Trabajadores.
</p>
<p>
Así mismo, la Dirección de Morés se compromete a aportar los medios y recursos necesarios para que las directrices establecidas por el sistema sean difundidas a todo el personal de la empresa para su conocimiento y entendimiento y a que sean implementadas para comprobar su efectividad. De la misma manera se promoverá que el sistema seguido por Morés sea también de conocimiento público, poniéndolo a disposición de clientes, proveedores y otras partes interesadas para el conocimiento del propósito de la Empresa en todos los ámbitos.
</p>  
<p>
La Dirección de Morés se compromete a que se determinen los requisitos del cliente que solicita papel certificado y se cumpla con ellos. Así mismo se exigirá a sus proveedores compromisos jurados que evidencien el cumplimiento de los requisitos de papel procedente de una gestión sostenible para poder seguir la cadena a su paso por nuestra empresa.
</p>  
<p>
Para conseguir que esta implementación sea eficaz, se precisa de la participación de todos los recursos disponibles de Morés, tanto humanos como técnicos.
</p>
<p>
La Dirección de Morés es la máxima responsable de velar por el buen funcionamiento y desarrollo del Sistema que toma como base la filosofía de una mejora continua, obtenida a través de métodos de seguimiento del proceso de cadena de custodia por nuestras actividades.
</p>
